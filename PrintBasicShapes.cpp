#include <iostream>

int main() {
    // Triangle
    std::cout << "    ^" << std::endl;
    std::cout << "   ^^^" << std::endl;
    std::cout << "  ^^^^^" << std::endl;
    std::cout << " ^^^^^^^" << std::endl;
    std::cout << "^^^^^^^^^" << std::endl;
    
    // Diamond
    std::cout << "    &" << std::endl;
    std::cout << "   & &" << std::endl;
    std::cout << "  &   &" << std::endl;
    std::cout << "   & &" << std::endl;
    std::cout << "    &" << std::endl;

    // Rectangle
    std::cout << "**********" << std::endl;
    std::cout << "*        *" << std::endl;
    std::cout << "*        *" << std::endl;
    std::cout << "*        *" << std::endl;
    std::cout << "**********" << std::endl;

    return 0;
}

