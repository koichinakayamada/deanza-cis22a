#include <iostream>
using namespace std;

int main() {
    // Triangle
    cout << "    ^" << std::endl;
    cout << "   ^^^" << std::endl;
    cout << "  ^^^^^" << std::endl;
    cout << " ^^^^^^^" << std::endl;
    cout << "^^^^^^^^^" << std::endl;
    
    // Diamond
    cout << "  &" << std::endl;
    cout << " & &" << std::endl;
    cout << "&   &" << std::endl;
    cout << " & &" << std::endl;
    cout << "  &" << std::endl;

    // Rectangle
    cout << "**********" << std::endl;
    cout << "*        *" << std::endl;
    cout << "*        *" << std::endl;
    cout << "*        *" << std::endl;
    cout << "**********" << std::endl;

    return 0;
}

